package spring.react.jwt.model.view;

public class CityOutputView {

    private Long id;
    private String name;

    public CityOutputView() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
